package com.how2j.interface2;

public abstract class Animal {
	
	/**
	 	1. 创建Animal类，它是所有动物的抽象父类。
		2. 声明一个受保护的整数类型属性legs，它记录动物的腿的数目。
		3. 定义一个受保护的构造器，用来初始化legs属性。
		4. 声明抽象方法eat。
		5. 声明具体方法walk来打印动物是如何行走的（包括腿的数目）
	 */
	    String name;
	    protected int legs;
	     
	    public Animal() {
	    	System.out.println("Animal默认构造器");
	    }
	     
	    protected Animal(int legs) {
	        this.legs = legs;
	    }
	    
	     
	    public abstract void eat();
	     
	    public void walk() {
	    	System.out.println("行走中");
	        System.out.println("是用"+this.legs+"条腿走路的~");
	    }
	    
	}
